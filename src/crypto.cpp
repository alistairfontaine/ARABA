// Round constant
static const uint8_t rcon[11] = {
    0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36
};

// Galois Field multiplication
static uint8_t gmul(uint8_t a, uint8_t b) {
    uint8_t p = 0;
    for (int i = 0; i < 8; i++) {
        if (b & 1) p ^= a;
        uint8_t hi = a & 0x80;
        a <<= 1;
        if (hi) a ^= 0x1b;
        b >>= 1;
    }
    return p;
}

// RotWord: rotate 4-byte word left by 1 byte
static void rot_word(uint8_t w[4]) {
    uint8_t t = w[0];
    w[0] = w[1];
    w[1] = w[2];
    w[2] = w[3];
    w[3] = t;
}

// SubWord: apply S-box to each byte
static void sub_word(uint8_t w[4]) {
    for (int i = 0; i < 4; i++) w[i] = sbox[w[i]];
}

AES256::AES256(const uint8_t key[AES_KEY_SIZE]) {
    key_expansion(key);
}

void AES256::key_expansion(const uint8_t key[AES_KEY_SIZE]) {
    // round_keys[round][byte] — flat 4x4 state per round
    // Total: 15 rounds * 16 bytes = 240 bytes

    uint8_t temp[4];
    int i = 0;

    // Copy the first 8 words (32 bytes) directly: word i = key bytes [4i .. 4i+3]
    while (i < 8) {
        for (int j = 0; j < 4; j++) {
            round_keys[i][j] = key[i * 4 + j];
        }
        i++;
    }

    i = 8;
    while (i < 60) { // 60 words for AES-256
        for (int j = 0; j < 4; j++) temp[j] = round_keys[i - 1][j];

        if (i % 8 == 0) {
            rot_word(temp);
            sub_word(temp);
            temp[0] ^= rcon[i / 8];
        } else if (i % 8 == 4) {
            sub_word(temp);
        }

        for (int j = 0; j < 4; j++) {
            round_keys[i][j] = round_keys[i - 8][j] ^ temp[j];
        }
        i++;
    }
}

void AES256::add_round_key(uint8_t state[AES_BLOCK_SIZE], int round) {
    for (int c = 0; c < 4; c++) {
        for (int j = 0; j < 4; j++) {
            state[j + 4 * c] ^= round_keys[4 * round + c][j];
        }
    }
}

void AES256::sub_bytes(uint8_t state[AES_BLOCK_SIZE]) {
    for (int i = 0; i < AES_BLOCK_SIZE; i++) state[i] = sbox[state[i]];
}

void AES256::inv_sub_bytes(uint8_t state[AES_BLOCK_SIZE]) {
    for (int i = 0; i < AES_BLOCK_SIZE; i++) state[i] = inv_sbox[state[i]];
}

void AES256::shift_rows(uint8_t state[AES_BLOCK_SIZE]) {
    uint8_t temp[AES_BLOCK_SIZE];
    std::memcpy(temp, state, AES_BLOCK_SIZE);
    // Row 0: no shift
    state[0]  = temp[0];  state[4]  = temp[4];  state[8]  = temp[8];  state[12] = temp[12];
    // Row 1: shift left by 1
    state[1]  = temp[5];  state[5]  = temp[9];  state[9]  = temp[13]; state[13] = temp[1];
    // Row 2: shift left by 2
    state[2]  = temp[10]; state[6]  = temp[14]; state[10] = temp[2];  state[14] = temp[6];
    // Row 3: shift left by 3
    state[3]  = temp[15]; state[7]  = temp[3];  state[11] = temp[7];  state[15] = temp[11];
}

void AES256::inv_shift_rows(uint8_t state[AES_BLOCK_SIZE]) {
    uint8_t temp[AES_BLOCK_SIZE];
    std::memcpy(temp, state, AES_BLOCK_SIZE);
    // Row 0: no shift
    state[0]  = temp[0];  state[4]  = temp[4];  state[8]  = temp[8];  state[12] = temp[12];
    // Row 1: shift right by 1
    state[1]  = temp[13]; state[5]  = temp[1];  state[9]  = temp[5];  state[13] = temp[9];
    // Row 2: shift right by 2
    state[2]  = temp[10]; state[6]  = temp[14]; state[10] = temp[2];  state[14] = temp[6];
    // Row 3: shift right by 3
    state[3]  = temp[7];  state[7]  = temp[11]; state[11] = temp[15]; state[15] = temp[3];
}

void AES256::mix_columns(uint8_t state[AES_BLOCK_SIZE]) {
    for (int c = 0; c < 4; c++) {
        uint8_t a[4], b[4];
        for (int i = 0; i < 4; i++) {
            a[i] = state[i + 4 * c];
            b[i] = gmul(state[i + 4 * c], 2);
        }
        state[0 + 4 * c] = b[0] ^ gmul(a[1], 3) ^ a[2] ^ a[3];
        state[1 + 4 * c] = a[0] ^ b[1] ^ gmul(a[2], 3) ^ a[3];
        state[2 + 4 * c] = a[0] ^ a[1] ^ b[2] ^ gmul(a[3], 3);
        state[3 + 4 * c] = gmul(a[0], 3) ^ a[1] ^ a[2] ^ b[3];
    }
}

void AES256::inv_mix_columns(uint8_t state[AES_BLOCK_SIZE]) {
    for (int c = 0; c < 4; c++) {
        uint8_t a[4];
        for (int i = 0; i < 4; i++) a[i] = state[i + 4 * c];
        state[0 + 4 * c] = gmul(a[0], 0x0e) ^ gmul(a[1], 0x0b) ^ gmul(a[2], 0x0d) ^ gmul(a[3], 0x09);
        state[1 + 4 * c] = gmul(a[0], 0x09) ^ gmul(a[1], 0x0e) ^ gmul(a[2], 0x0b) ^ gmul(a[3], 0x0d);
        state[2 + 4 * c] = gmul(a[0], 0x0d) ^ gmul(a[1], 0x09) ^ gmul(a[2], 0x0e) ^ gmul(a[3], 0x0b);
        state[3 + 4 * c] = gmul(a[0], 0x0b) ^ gmul(a[1], 0x0d) ^ gmul(a[2], 0x09) ^ gmul(a[3], 0x0e);
    }
}

void AES256::encrypt_block(uint8_t block[AES_BLOCK_SIZE]) {
    add_round_key(block, 0);
    for (int round = 1; round < AES_ROUNDS; round++) {
        sub_bytes(block);
        shift_rows(block);
        mix_columns(block);
        add_round_key(block, round);
    }
    sub_bytes(block);
    shift_rows(block);
    add_round_key(block, AES_ROUNDS);
}

void AES256::decrypt_block(uint8_t block[AES_BLOCK_SIZE]) {
    add_round_key(block, AES_ROUNDS);
    for (int round = AES_ROUNDS - 1; round > 0; round--) {
        inv_shift_rows(block);
        inv_sub_bytes(block);
        add_round_key(block, round);
        inv_mix_columns(block);
    }
    inv_shift_rows(block);
    inv_sub_bytes(block);
    add_round_key(block, 0);
}

void AES256::encrypt_cbc(uint8_t* data, size_t data_len, const uint8_t iv[AES_BLOCK_SIZE]) {
    uint8_t prev[AES_BLOCK_SIZE];
    std::memcpy(prev, iv, AES_BLOCK_SIZE);

    for (size_t i = 0; i < data_len; i += AES_BLOCK_SIZE) {
        for (int j = 0; j < AES_BLOCK_SIZE; j++) {
            data[i + j] ^= prev[j];
        }
        encrypt_block(data + i);
        std::memcpy(prev, data + i, AES_BLOCK_SIZE);
    }
}

void AES256::decrypt_cbc(uint8_t* data, size_t data_len, const uint8_t iv[AES_BLOCK_SIZE]) {
    uint8_t prev[AES_BLOCK_SIZE], temp[AES_BLOCK_SIZE];
    std::memcpy(prev, iv, AES_BLOCK_SIZE);

    for (size_t i = 0; i < data_len; i += AES_BLOCK_SIZE) {
        std::memcpy(temp, data + i, AES_BLOCK_SIZE);
        decrypt_block(data + i);
        for (int j = 0; j < AES_BLOCK_SIZE; j++) {
            data[i + j] ^= prev[j];
        }
        std::memcpy(prev, temp, AES_BLOCK_SIZE);
    }
}

void derive_key(const char* passphrase, uint8_t key[AES_KEY_SIZE]) {
    std::memset(key, 0, AES_KEY_SIZE);
    size_t len = std::strlen(passphrase);
    for (size_t i = 0; i < AES_KEY_SIZE; i++) {
        key[i] = passphrase[i % len];
        key[i] = (key[i] * 31 + i) ^ 0x5A;
    }
}

void generate_iv(uint8_t iv[AES_BLOCK_SIZE]) {
    auto now = std::chrono::steady_clock::now().time_since_epoch().count();
    for (int i = 0; i < AES_BLOCK_SIZE; i++) {
        iv[i] = (now >> (i * 4)) & 0xFF;
    }
}

void derive_pair_key(
    const uint8_t mac_a[6],
    const uint8_t mac_b[6],
    const uint8_t base_key[AES_KEY_SIZE],
    uint8_t out_key[AES_KEY_SIZE]
) {
    const uint8_t* first = mac_a;
    const uint8_t* second = mac_b;

    int cmp = std::memcmp(mac_a, mac_b, 6);
    if (cmp > 0) {
        first = mac_b;
        second = mac_a;
    }

    std::memcpy(out_key, base_key, AES_KEY_SIZE);
    for (int i = 0; i < 6; i++) {
        out_key[i] ^= first[i];
        out_key[i + 16] ^= second[i];
    }

    for (int i = 0; i < AES_KEY_SIZE; i++) {
        out_key[i] = (out_key[i] * 17 + i) ^ 0xA5;
    }
}

